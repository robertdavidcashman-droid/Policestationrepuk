#!/usr/bin/env node
/** Generates publish-ready packages under seo-growth/sites/ — run after external-content-data.mjs exists */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { SITE_META, topicContent, expandSections, wordCount } from './external-content-data.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');
const OUT = path.join(ROOT, 'seo-growth');
const DISCLAIMER = 'General information only. Not legal advice on specific facts. No guarantee of outcome.';

const POSTS = JSON.parse(fs.readFileSync(path.join(__dirname, 'posts-config.json'), 'utf8'));

function ensureDir(p) { fs.mkdirSync(p, { recursive: true }); }

function metaDesc(title, keyword) {
  const d = `Practical UK guidance: ${title}. Covers ${keyword} for criminal defence professionals. General information only.`;
  return d.length <= 160 ? d : d.slice(0, 157) + '…';
}

function buildBlog(siteKey, post) {
  const site = SITE_META[siteKey];
  const topic = topicContent(siteKey, post.slug, post.title, post.keyword);
  const body = expandSections(topic.sections);
  const faq = topic.faq.map((f) => `### ${f.q}\n\n${f.a}`).join('\n\n');
  const cross = site.crossLinks.map((l) => `- [${l.label}](${l.url})`).join('\n');
  return `---
title: "${post.title.replace(/"/g, '\\"')}"
metaTitle: "${post.title.slice(0, 58).replace(/"/g, '\\"')}"
metaDescription: "${metaDesc(post.title, post.keyword).replace(/"/g, '\\"')}"
slug: ${post.slug}
canonical: ${site.baseUrl}/blog/${post.slug}
site: ${site.domain}
published: draft
author: Editorial team
lastUpdated: 2026-06-14
cta: ${site.ctas[0]}
schema: Article
primaryKeyword: ${post.keyword}
---

# ${post.title}

**Last updated:** 14 June 2026

## Quick answer

${topic.intro}

## Who this is for

- ${site.audience}

${body}

## FAQ

${faq}

## Related resources

${cross}

## Next step

**${site.ctas[0]}** — [${site.baseUrl}](${site.baseUrl})

---

*${DISCLAIMER}*
`;
}

function schemaArticle(siteKey, post) {
  const site = SITE_META[siteKey];
  return {
    '@context': 'https://schema.org',
    '@type': 'Article',
    headline: post.title,
    dateModified: '2026-06-14',
    author: { '@type': 'Organization', name: site.name },
    publisher: { '@type': 'Organization', name: site.name, url: site.baseUrl },
    mainEntityOfPage: `${site.baseUrl}/blog/${post.slug}`,
  };
}

function writeSiteReadme(domain, siteKey, counts) {
  const site = SITE_META[siteKey];
  const dir = path.join(OUT, 'sites', domain);
  ensureDir(dir);
  fs.writeFileSync(path.join(dir, 'README.md'), `# ${domain} publish package\n\nBlogs: ${counts.blogs}\nLocal pages: ${counts.local}\n\nSee MANUAL_PUBLICATION_TODO.md\n`);
  fs.writeFileSync(path.join(dir, 'MANUAL_PUBLICATION_TODO.md'), `# ${domain}\n\n1. Copy public/llms.txt\n2. Publish blog/*.md\n3. Publish local/*.md\n4. Add schema/*.json\n5. CTAs: ${site.ctas.join(', ')}\n`);
  const llmsSrc = path.join(OUT, 'llms', `${domain}-llms.txt`);
  if (fs.existsSync(llmsSrc)) {
    ensureDir(path.join(dir, 'public'));
    fs.copyFileSync(llmsSrc, path.join(dir, 'public', 'llms.txt'));
  }
}

for (const [siteKey, posts] of Object.entries(POSTS)) {
  const site = SITE_META[siteKey];
  const siteDir = path.join(OUT, 'sites', site.domain);
  ensureDir(path.join(siteDir, 'blog'));
  ensureDir(path.join(siteDir, 'schema'));
  let minWords = 9999;
  for (const post of posts) {
    const md = buildBlog(siteKey, post);
    fs.writeFileSync(path.join(siteDir, 'blog', `${post.slug}.md`), md);
    fs.writeFileSync(path.join(siteDir, 'schema', `blog-${post.slug}.json`), JSON.stringify(schemaArticle(siteKey, post), null, 2));
    fs.writeFileSync(path.join(OUT, 'blog-posts', siteKey, `${post.slug}.md`), md);
    minWords = Math.min(minWords, wordCount(md));
  }
  writeSiteReadme(site.domain, siteKey, { blogs: posts.length, local: 0 });
  console.log(site.domain, posts.length, 'blogs, min words', minWords);
}

console.log('Done — run generate-external-content.mjs for local pages and buffer CSV');
